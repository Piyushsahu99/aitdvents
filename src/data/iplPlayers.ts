// Helper function to format price in Crores/Lakhs
export const formatPrice = (lakhs: number): string => {
  if (lakhs >= 100) {
    return `₹${(lakhs / 100).toFixed(1)} Cr`;
  }
  return `₹${lakhs} L`;
};

// Get players by category
export const getPlayersByCategory = (category: IPLPlayer["category"]) => 
  iplPlayers.filter(p => p.category === category);

